function makeWaves(blueprint: Array<Array<[EnemyKind, number, number]>>) {
  return blueprint.map((wave, i) => ({
    index: i,
    spawns: wave.map(([kind, count, spawnInterval]) => ({ kind, count, spawnInterval })),
  }));
}

